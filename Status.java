package com.db.legends.model;

public class Status {

	private int power;
	
	private int health;
	
	private int strikeAtk;
	
	private int strikeDef;
	
	private int blastAtk;
	
	private int blastDef;
	
	private int slice;
	
	private int pierce;
	
	private int impact;
	
	private int explode;
	
	public int getPower() {
		return power;
	}

	public void setPower(int power) {
		this.power = power;
	}

	public int getHealth() {
		return health;
	}

	public void setHealth(int health) {
		this.health = health;
	}

	public int getStrikeAtk() {
		return strikeAtk;
	}

	public void setStrikeAtk(int strikeAtk) {
		this.strikeAtk = strikeAtk;
	}

	public int getStrikeDef() {
		return strikeDef;
	}

	public void setStrikeDef(int strikeDef) {
		this.strikeDef = strikeDef;
	}

	public int getBlastAtk() {
		return blastAtk;
	}

	public void setBlastAtk(int blastAtk) {
		this.blastAtk = blastAtk;
	}

	public int getBlastDef() {
		return blastDef;
	}

	public void setBlastDef(int blastDef) {
		this.blastDef = blastDef;
	}

	public int getSlice() {
		return slice;
	}

	public void setSlice(int slice) {
		this.slice = slice;
	}

	public int getPierce() {
		return pierce;
	}

	public void setPierce(int pierce) {
		this.pierce = pierce;
	}

	public int getImpact() {
		return impact;
	}

	public void setImpact(int impact) {
		this.impact = impact;
	}

	public int getExplode() {
		return explode;
	}

	public void setExplode(int explode) {
		this.explode = explode;
	}

	public int getVanish() {
		return vanish;
	}

	public void setVanish(int vanish) {
		this.vanish = vanish;
	}

	public int getKiRestore() {
		return kiRestore;
	}

	public void setKiRestore(int kiRestore) {
		this.kiRestore = kiRestore;
	}

	public int getCritical() {
		return critical;
	}

	public void setCritical(int critical) {
		this.critical = critical;
	}

	private int vanish;
	
	private int kiRestore;
	
	private int  critical;
	
}
